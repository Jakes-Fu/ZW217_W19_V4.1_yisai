/**********************************************************************
//    Description:
//      Select a file in the SIM card file system,the file select rule
//    refference to GSM 11.11 section 6.5 "Methods for selecting a file"
//    Global resource dependence :
//    Author:Raislin.kong
//    Note:
***********************************************************************/
int8 SIMFS_FileRouting( //reutrn value:the number of file in the routing to the destionation file
    SIM_FILE_NAME_E file_name,  //the desitination file
    SIM_FILE_NAME_E cur_file,   //the current selected direction file
    SIM_FILE_NAME_E *file_route //the file route to the destionation file
)
{
    SIM_FILE_NAME_E file_route1[SIM_FILE_LEVEL_E_NUM] = {0};
    SIM_FILE_NAME_E file_route2[SIM_FILE_LEVEL_E_NUM] = {0};
    uint8   num1 = 0, num2 = 0, i;
    int8 file_num = 0;
    SIM_FILE_LEVEL_E    file_level1, file_level2;
    SIM_FILE_NAME_E file_name1 = file_name;
    SIM_FILE_NAME_E file_name2 = cur_file;
    SIM_FILE_NAME_E    *ptemp = file_route;
    BOOLEAN via_mf_flag = FALSE;

    //the invalidate sim file name
    if(file_name >= SIM_FILE_TOTAL_NUM)
    {
        return (int8) - 2;
    }

    //the sim still not pass ATR
    if(SIM_FILE_TOTAL_NUM <= cur_file)
    {
        return (int8) - 1;
    }

    //the MF can be always selected
    if(SIM_FILE_MF == file_name)
    {
        *ptemp = SIM_FILE_MF;
        ptemp += 1;
        file_num++;

        return file_num;
    }

    //Wether we should switch between one DF to another.(between DFtele/DFgsm/ADFusim).wuding
    if(SIMFS_IsNeedViaMF(file_name1, file_name2))
    {
        file_name2 = SIM_FILE_MF;
        via_mf_flag = TRUE;
    }

    //get the file that must select to select the destination file
    //they do not have the same parent file
    for(;;)
    {
        //the wanted selected file is the current Directory File
        if(file_name1 == file_name2)
        {
            file_route1[num1] = file_name1;
            num1++;
            break;
        }

        //the wanted selected file is the immediate child of
        //the current Directory File
        if(s_sim_file_system[file_name1][1] == file_name2)
        {
            file_route1[num1] = file_name1;
            num1++;
            break;
        }

        //the wanted selected file is an immediate child DF file
        //of the parent of the current directory file
        if(s_sim_file_system[file_name1][1] == s_sim_file_system[file_name2][1])
        {
            file_route1[num1] = file_name1;
            num1++;

            //if the wanted to select file is a ef file,then first
            //select the parent fie of the current directory file
            if(SIMFS_GetFileType(file_name1) == SIM_FILE_T_EF)
            {
                file_name2 = (SIM_FILE_NAME_E)s_sim_file_system[file_name2][1];
                file_route2[num2] = file_name2;
                num2++;
            }

            break;
        }

        //the wanted selected file is the prarent of the current directory file
        if(file_name1 == s_sim_file_system[file_name2][1])
        {
            file_route1[num1] = file_name1;
            num1++;
            break;
        }

        //else the lowest one level up to one level
        file_level1 = SIMFS_GetFileLevel(s_sim_file_system[file_name1][0]);
        file_level2 = SIMFS_GetFileLevel(s_sim_file_system[file_name2][0]);

        if(file_level1 > file_level2)
        {
            file_route1[num1] = file_name1;
            num1++;
            file_name1 = (SIM_FILE_NAME_E)s_sim_file_system[file_name1][1];
        }
        else if(file_level1 < file_level2)
        {
            //select the parent DF
            file_name2 = (SIM_FILE_NAME_E)s_sim_file_system[file_name2][1];
            //add the parent DF file  to the routing
            file_route2[num2] = file_name2;
            num2++;
        }
        //file_level1 == file_level2,both of two must get up one level
        else
        {
            file_route1[num1] = file_name1;
            num1++;
            file_name1 = (SIM_FILE_NAME_E)s_sim_file_system[file_name1][1];

            file_name2 = (SIM_FILE_NAME_E)s_sim_file_system[file_name2][1];
            file_route2[num2] = file_name2;
            num2++;
        }
    }

    //add the selection instruction to the instruction list
    i = 0;

    while(num2 > 0)
    {
        *ptemp = file_route2[i];
        ptemp += 1;
        file_num++;
        i++;
        num2--;
    }

    if(via_mf_flag)
    {
        file_route1[num1] = SIM_FILE_MF;
        num1++;
    }

    while(num1 > 0)
    {
        *ptemp = file_route1[num1 - 1];
        ptemp += 1;
        file_num++;
        num1--;
    }

    //return the total select instruction num
    return file_num;
}

