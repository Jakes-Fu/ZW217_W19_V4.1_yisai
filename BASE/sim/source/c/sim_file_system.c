/**********************************************************************
//    Description:
//      get the a EF file's file ID
//    Global resource dependence :
//    Author:Raislin.kong
//    Note:
***********************************************************************/
uint16  SIMFS_GetFileID(    //return value:the file ID
    uint32 cur_sim_task,
    SIM_FILE_NAME_E file_name     //the file name
)
{
    uint16 file_id = 0;

    //the file name is not a validate name to sim card
    if(SIM_IS_INVALIDE_FILE(file_name))
    {
        return 0xffff;
    }

    if((s_dcs1800_sim[cur_sim_task]) && (SIM_FILE_DF_GSM == file_name))
    {
        file_id = s_sim_file_system[SIM_FILE_DF_DCS1800][0];
    }
    else
    {
        file_id = s_sim_file_system[file_name][0];
    }

#ifdef SIM_3G_USIM_SUPPORT
    return SIMFS_RemapFileID(cur_sim_task, file_name, file_id);
#else
    return file_id;
#endif
}


