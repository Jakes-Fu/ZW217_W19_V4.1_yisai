#ifndef CONFIG_AUDIO_ONLY
#define JPEG_ENCODE_OUT_BUFFER_SIZE  (1024 * 24)
static void __video_encode(uint8_t *data, uint32_t data_bytes_len, avc_media_info_t *data_info)
{
    uint8_t          *encode_buffer = NULL;
    avc_media_info_t media_info;
    uint32_t         encode_buffer_len = JPEG_ENCODE_OUT_BUFFER_SIZE;
    ISP_FRAME_T      *frame = (ISP_FRAME_T *)data;
    int              err;
    avc_platform_t   *w217 = __get_w217_instance();

    LOGD(TAG, "video encode process.");
    encode_buffer = malloc(JPEG_ENCODE_OUT_BUFFER_SIZE);
    assert(encode_buffer);

    err = __jpeg_encode(frame->yaddr, data_bytes_len, frame->width, frame->height, encode_buffer, &encode_buffer_len);
    if (err != 0) {
        goto L_END;
    }

    w217->cc_avoid = fps_cc_avoid_trigger(w217->fps_cc_handle, encode_buffer_len, w217->target_send_bps);
    if (w217->cc_avoid) {
        LOGT(TAG, "send bps avoid trigger");
    }

    if (encode_buffer_len > 0 && !w217->cc_avoid) {
        media_info.data_type                 = AVC_DATA_VIDEO;
        media_info.u.video_info.codec        = VIDEO_CODEC_GENERIC_JPEG;
        media_info.u.video_info.is_key_frame = 1;
        media_info.u.video_info.fps          = CAPTURE_FPS_CC_AVOID_DEFAULT;
        media_info.u.video_info.sent_ts      = SCI_GetTickCount();
        w217->encoded_data_cb(encode_buffer, encode_buffer_len, &media_info);
        LOGD(TAG, "finish encoded data push.");
    }

L_END:
    free(encode_buffer);
}
#endif

#define AUDIO_ENCODER_BUF_SIZE (160 * PER_AUDIO_PACKET_CONTAINS_20MS_FRAMES_CNT)
static void __audio_encode(uint8_t *data, uint32_t data_bytes_len, avc_media_info_t *data_info)
{
    avc_media_info_t media_info;
    uint8_t          audio_encoder_buffer[AUDIO_ENCODER_BUF_SIZE];
    uint32_t         encoded_len;
    avc_platform_t   *w217 = __get_w217_instance();

    encoded_len = g722_encode(&w217->g722_encode_state, audio_encoder_buffer, data, data_bytes_len / sizeof(uint16_t));

    LOGD(TAG, "audio encode process. l1=%d, l2=%d", data_bytes_len, encoded_len);

    media_info.data_type            = AVC_DATA_AUDIO;
    media_info.u.audio_info.codec   = AUDIO_CODEC_G722;
    media_info.u.audio_info.sent_ts = SCI_GetTickCount();
    w217->encoded_data_cb(audio_encoder_buffer, encoded_len, &media_info);
}

static void __avc_platform_encoding(uint8_t *data, size_t len, avc_media_info_t *media_info)
{
    switch (media_info->data_type) {
        case AVC_DATA_AUDIO:
            __audio_encode(data, len, media_info);
            break;
        case AVC_DATA_VIDEO:
#ifndef CONFIG_AUDIO_ONLY
            __video_encode(data, len, media_info);
#endif
            break;
        default:
            break;
    }
}

