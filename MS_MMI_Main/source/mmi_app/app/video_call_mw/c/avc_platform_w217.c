static HAUDIO hvoiphandle = INVALID_HANDLE;
extern HAUDIODEV hAUDDEVDUAL;
extern HAUDIOCODEC hVoipCodec;
static void __voip_cap_callback(uint16 *pcm, uint32 *uiLength, void *param)
{
    avc_platform_t *w217 = __get_w217_instance();
    LOGT(TAG, "[VideoCallAudio] record callback. addr=%x, len=%d", pcm, *uiLength);
    assert(pcm && uiLength);

    if (-1 == avc_ringbuf_write(w217->ringbuf_audio_helper, pcm, *uiLength)) {
        LOGE(TAG, "audio helper buffer overrun");
    }
}

static void __voip_play_callback(uint16 *dst, uint32 *puiLength, void *param)
{
    avc_platform_t *w217 = __get_w217_instance();
    uint8 pcm_data[320];

    LOGT(TAG, "Addr=0x%x, len=%u", dst, *puiLength);

    if (-1 == avc_ringbuf_read(pcm_data, sizeof(pcm_data), w217->ringbuf_audio_handle)) {
        memset(pcm_data, 0, sizeof(pcm_data));
    }

    memcpy(dst, pcm_data, sizeof(pcm_data));
}

static void __audio_dummy_notify_callback(HAUDIO hAudio, uint32 notify_info, uint32 affix_info)
{}

static void __voip_start(void)
{
    VOIP_EXT_OPER_START_PARA_T tPara = {0};
    uint32 aud_result;
    int32 err;

    if (hvoiphandle != INVALID_HANDLE) {
        LOGW(TAG, "voip started. duplicated process, ignore");
        return;
    }

    hvoiphandle = AUDIO_CreateNormalHandle(hVoipCodec, SCI_NULL, hAUDDEVDUAL, SCI_NULL, __audio_dummy_notify_callback);
    LOGT(TAG, "hvoiphandle=%u", hvoiphandle);
    assert(hvoiphandle);

    err = AUD_EnableVoiceProcessConfig(SCI_TRUE, SCI_FALSE, PNULL);
    LOGT(TAG, "AUD_EnableVoiceProcessConfig err=%d", err);
    assert(err == 0);

    tPara.hAudioHandle     = hvoiphandle;
    tPara.para0            = AUDIO_DATA_EXCHANGE_VOIP;
    tPara.para1            = 8000;
    tPara.para2            = 160;
    tPara.para3            = AUDIO_MODE_APP_TYPE_VOIP_1;
    tPara.readdataClkFunc  = __voip_play_callback;
    tPara.writedataClkFunc = __voip_cap_callback;
    tPara.readparam        = 0;
    tPara.writeparam       = 0;
    err = AUDIO_ExeCodecExtOpe(tPara.hAudioHandle,
                               ANSI2UINT16("VOIP_EXT_START"),
                               4,
                               &tPara,
                               &aud_result);
    LOGT(TAG, "aud_voip:voip start 1 Sa=%d", tPara.para1);
    assert(err == 0);
}

static void __voip_stop(void)
{
    VOIP_EXT_OPER_STOP_PARA_T tStopPara = {0};
    uint32 aud_result;
    int32 err;

    if (!hvoiphandle) {
        LOGE(TAG, "hvoiphandle null");
        return;
    }

    tStopPara.hAudioHandle = hvoiphandle;
    tStopPara.para0        = AUDIO_DATA_EXCHANGE_VOIP;
    err = AUDIO_ExeCodecExtOpe(tStopPara.hAudioHandle, ANSI2UINT16("VOIP_EXT_STOP"), 0, &tStopPara, &aud_result);
    LOGT(TAG, "AUDIO_ExeCodecExtOpe err=%d", err);
    assert(err == 0);

    err = AUD_EnableVoiceProcessConfig(SCI_FALSE, SCI_FALSE, PNULL);
    LOGT(TAG, "AUD_EnableVoiceProcessConfig err=%d", err);
    assert(err == 0);

    err = AUDIO_CloseHandle(hvoiphandle);
    LOGT(TAG, "AUDIO_CloseHandle err=%d", err);
    assert(err == 0);

    hvoiphandle = 0;
    LOGT(TAG, "aud_voip:voip stop");
}

static void __audio_helper_task(int argc, char *argv)
{
    avc_media_info_t media_info;
    uint8_t          audio_data[320 * PER_AUDIO_PACKET_CONTAINS_20MS_FRAMES_CNT];
    uint32_t         audio_data_len;
    avc_platform_t   *w217 = __get_w217_instance();

    media_info.data_type          = AVC_DATA_AUDIO;
    media_info.u.audio_info.codec = AUDIO_CODEC_GENERIC;

    while (w217->b_audio_helper_running) {
        audio_data_len = avc_ringbuf_getdatasize(w217->ringbuf_audio_helper);
        if (audio_data_len < sizeof(audio_data)) {
            SCI_Sleep(PER_AUDIO_PACKET_CONTAINS_20MS_FRAMES_CNT * 16);
            continue;
        }

        avc_ringbuf_read(audio_data, sizeof(audio_data), w217->ringbuf_audio_helper);

        media_info.u.audio_info.sent_ts = SCI_GetTickCount();
 
        if (!__is_memory_lack_threshold_trigger()) {
            w217->audio_data_cb(audio_data, sizeof(audio_data), &media_info);
        }
    }

    w217->b_audio_helper_closed = true;
    SCI_ThreadExit();
}

#define AUDIO_HELPER_BUF_SIZE (320 * PER_AUDIO_PACKET_CONTAINS_20MS_FRAMES_CNT * 8)
static void __audio_capture_helper_create()
{
    avc_platform_t *w217 = __get_w217_instance();

    w217->ringbuf_audio_helper = avc_ringbuf_create(AUDIO_HELPER_BUF_SIZE);
    assert(w217->ringbuf_audio_helper);

    w217->b_audio_helper_running = true;
    SCI_CreateThread("audio_helper",       // Name string of the thread
                     "q_audio_helper",     // Queue name string of the thread
                     __audio_helper_task,  // Entry function of the thread
                     1,                    // First parameter for entry function,
                     NULL,                 // Second parameter for entry function,
                     4096,                 // Size of the thread stack in bytes
                     200,                  // Number of messages which can be enqueued
                     AVC_TASK_PRIORITY,    // Priority of the thread.
                     SCI_PREEMPT,          // Indicates if the thread is preemptable.
                     SCI_AUTO_START);
}

