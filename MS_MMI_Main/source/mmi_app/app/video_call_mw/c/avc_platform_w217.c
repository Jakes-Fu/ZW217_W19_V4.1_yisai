#ifndef CONFIG_AUDIO_ONLY
static int32_t __jpeg_encode(uint8_t *original_frame, uint32_t original_frame_len,
                             uint32_t width, uint32_t height,
                             uint8_t *encode_out_buf, uint32_t *encode_out_buf_len)
{
    JPEG_ENC_RET_E       jpeg_ret  = JPEG_ENC_OK;
    JPEG_ENC_IN_PARAM_T  in_param  = {0};
    JPEG_ENC_OUT_PARAM_T out_param = {0};
    avc_platform_t *w217           = __get_w217_instance();

    in_param.enc_data_type = JPEG_ENC_DATA_TYPE_YUV422;
    in_param.image_rect.x  = 0;
    in_param.image_rect.y  = 0;
    in_param.image_rect.w  = width;
    in_param.image_rect.h  = height;
    in_param.quality_level = w217->cc_avoid ? 0 : 2;
    in_param.jpeg_buf_ptr  = encode_out_buf;
    in_param.jpeg_buf_size = *encode_out_buf_len;

    in_param.yuv_addr.y_chn_addr = original_frame;
    in_param.yuv_addr.u_chn_addr = 0;
    in_param.yuv_addr.v_chn_addr = 0;

    in_param.dct_mode            = JPEG_DCT_FAST;
    in_param.reserved            = 0;//1 rotation 90

    jpeg_ret = JPEG_EncodeJpeg(&in_param, &out_param);
    if (JPEG_ENC_OK != jpeg_ret) {
        LOGE(TAG, "JPEG ENCODE ERR!!");
        return -1;
    }

    if (0 == out_param.out_size) {
        LOGE(TAG, "UPCC_Camera.c->UPCC_GetAndSendFrame(): one frame lost.");
        return -1;
    }

    *encode_out_buf_len = out_param.out_size;
    LOGD(TAG, "JPEG ENCODE done. len=%d!!", out_param.out_size);
    return 0;
}
#endif

