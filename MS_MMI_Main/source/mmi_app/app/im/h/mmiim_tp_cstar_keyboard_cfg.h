/*
输入法键盘状态类型
目标：把各种虚拟键盘可能展现的方式都列出来，
表示一个状态。但是由于大小写之间切换键盘对应的位置都没有发生变化
且键盘上也有快捷键可以进行大小写切换，帮省去大小写的状态，交由程序自己处理
9/26键盘对应完全不一样，故分开，多国语同理。
状态的顺序很重要，由于多国语关系总数较多，编护时千万不要弄混了，
建议写上五个五个写一堆，多国语需要增加语言支持的宏开关
*/

