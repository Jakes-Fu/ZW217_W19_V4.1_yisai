                SCI_TRACE_ID(TRACE_TOOL_CONVERT,MMIIM_TOUCH_APP_SOGOU_1338_112_2_18_2_26_59_84,(uint8*)"dd", point.x, point.y);
                is_omit_msg = !hw_result;
                break;
