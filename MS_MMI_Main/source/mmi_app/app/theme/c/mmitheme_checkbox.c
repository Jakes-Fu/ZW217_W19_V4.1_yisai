//  Description : get checkbox theme
//  Global resource dependence : 
//  Author: hua.fang
//  Note:
/*****************************************************************************/
void MMITHEME_GetCheckboxTheme(
                               MMITHEME_CHECKBOX_T    *theme_ptr  //in/out:
                               )
{
    //set check image
    theme_ptr->check.unselect = IMAGE_CHECK_UNSELECTED_ICON;
    theme_ptr->check.select   = IMAGE_CHECK_SELECTED_ICON;
    theme_ptr->check.gray     = IMAGE_CHECK_UNSELECTED_ICON;
}

