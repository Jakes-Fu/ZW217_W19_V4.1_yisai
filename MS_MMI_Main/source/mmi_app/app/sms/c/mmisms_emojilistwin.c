    tip_style.font_color = win_data_ptr->color_font;
    tip_style.align = ALIGN_LVMIDDLE;
    GUISTR_DrawTextToLCDInRect(&win_data_ptr->lcd_dev_info,
                               &win_data_ptr->title_rect,
                               &win_data_ptr->title_rect,
                               &prompt_str,
                               &tip_style,
                               GUISTR_STATE_NONE,
                               GUISTR_TEXT_DIR_AUTO);

    //draw page tip
    tip_style.align = ALIGN_RVMIDDLE;
    sprintf(tip_str, "(%d/%d)",
        win_data_ptr->first_row / win_data_ptr->row + 1,
        win_data_ptr->row_cnt / win_data_ptr->row + ((win_data_ptr->row_cnt % win_data_ptr->row) ? 1 : 0));
    for (i=0; i<8; i++) tip_wstr[i] = tip_str[i];
    tip_info.wstr_ptr = tip_wstr;
    tip_info.wstr_len = MMIAPICOM_Wstrlen(tip_wstr);
    GUISTR_DrawTextToLCDInRect(&win_data_ptr->lcd_dev_info,
        &win_data_ptr->title_rect,
        &win_data_ptr->title_rect,
        &tip_info,
        &tip_style,
        GUISTR_STATE_NONE,
        GUISTR_TEXT_DIR_AUTO);

    //cal row and column
    win_data_ptr->rect_emoji.bottom = win_data_ptr->rect_emoji.top + MMISMS_EMOJI_HEIGHT * MMISMS_EMOJI_ROW ; //240;
    rect_width = win_data_ptr->rect_emoji.right - win_data_ptr->rect_emoji.left + 1;
    rect_height = win_data_ptr->rect_emoji.bottom - win_data_ptr->rect_emoji.top + 1;
    unit_width = rect_width / win_data_ptr->col;//每个单元格宽度60
    unit_height = rect_height / win_data_ptr->row;

    //hot
    GetHotPos(win_data_ptr, &hot_row, &hot_col);

    //SCI_ASSERT(hot_row >= 0); /*assert verified*/
    if(hot_row < 0)
    {
        return;
    }
    
    hot_rect.top = win_data_ptr->rect_emoji.top + unit_height * hot_row;
    hot_rect.left = win_data_ptr->rect_emoji.left + unit_width * hot_col;
