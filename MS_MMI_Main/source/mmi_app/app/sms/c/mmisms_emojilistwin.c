//    win_data_ptr->key_pagedown.str[0] = '[';
//    win_data_ptr->key_pagedown.str[1] = '#';
//    win_data_ptr->key_pagedown.str[2] = ']';
    win_data_ptr->key_pagedown.str[0] = '>';
    win_data_ptr->key_pagedown.str[1] = '>';
