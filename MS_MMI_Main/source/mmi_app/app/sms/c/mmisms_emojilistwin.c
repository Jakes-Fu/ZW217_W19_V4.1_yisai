        wchar symbol_wstr[3] = {0};

        //没有符号了，则不画了
        if (symb_first + symb_idx >= win_data_ptr->emoji_cnt)
            break;

        symb_row = symb_idx / win_data_ptr->col;
        symb_col = symb_idx % win_data_ptr->col;
        GetEmojiRect(win_data_ptr, symb_row, symb_col, &rect_symb);

        index = symb_first + symb_idx;
        symb_info.wstr_ptr = symbol_wstr;
        MMIAPICOM_Wstrncpy(symb_info.wstr_ptr, &s_emoji_image_text_convert_table[index].data, 1);
//        MMIAPICOM_Wstrcat(symb_info.wstr_ptr, space);
