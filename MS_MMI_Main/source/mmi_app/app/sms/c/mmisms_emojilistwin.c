    wchar tip_wstr[8] = {0}; //tip string, limit is (99/99)
    uint16 unit_width = 0;
    uint16 unit_height = 0;
    uint16 rect_width = 0;
    uint16 rect_height = 0;

    GUI_RECT_T hot_rect = {0};
    int16 hot_row = 0;
    int16 hot_col = 0;
    uint32 symb_first = 0;
    uint32 symb_per_page = 0;
    uint32 symb_idx = 0;
    uint16 symb_row = 0;
    uint16 symb_col = 0;
    uint8 i = 0;
    GUI_RECT_T rect_symb = {0};
    MMI_STRING_T symb_info = {0};
    GUISTR_STYLE_T symb_style = {0};
    uint16 index = 0;

    if (PNULL == win_data_ptr)
    {
        return;
    }

    //draw...
    //draw panel
    LCD_FillRect(&(win_data_ptr->lcd_dev_info),
                 win_data_ptr->win_rect,
                 win_data_ptr->color_bg);

    MMISMS_EMOJI_ComKeyDraw(&win_data_ptr->lcd_dev_info, &win_data_ptr->key_pageup);
    MMISMS_EMOJI_ComKeyDraw(&win_data_ptr->lcd_dev_info, &win_data_ptr->key_pagedown);

