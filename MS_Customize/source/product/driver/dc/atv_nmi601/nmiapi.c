static void nmi_atv_deinit(void)
{
	tNmiDrv *pd = &nmiDrv;

	if (pd->poll) {
		//nmi_poll_stop();
	}
	nmi_os_del_mutex(pd->mu);
	nmi_i2c_deinit();
	nmi_power_down();
	already_init = 0;
}

static tNmiRetCode nmi_atv_init(tAtvInit *p)
{
	tNmiDrv *pd = &nmiDrv;
	tNmiRetCode rc = NMI_RET_OK;
	tNmiIn inp;

	if (!already_init) {
		memset((void *)pd, 0, sizeof(tNmiDrv));
		memset((void *)&inp, 0, sizeof(tNmiIn));

		pd->cRgn=0xffff;

		/**
			check input parameters.
		**/
		if (p->xo == nXO26MHz)
			inp.xo = 26;
		else {
			rc = NMI_RET_BAD_PARAMETER;
			goto _exit_;
		}

		if (p->format == nRGB) {
			inp.btformat = nBTRGB;
		} else if (p->format == nYCbCr) {
			inp.btformat = nBTYCbCr;
		} else {
			rc = NMI_RET_BAD_PARAMETER;
			goto _exit_;
		}

		if (p->rate == nBT27MHz) {
			inp.btrate = nClk27;
		} else if (p->rate == nBT13_5MHz) {
			inp.btrate = nClk13_5;
		} else if (p->rate == nBT6_75MHz) {
			inp.btrate = nClk6_75;
		} else {
			rc = NMI_RET_BAD_PARAMETER;
			goto _exit_;
		}
		
		inp.io= p->io_volt;
        inp.size=p->size;
		inp.clk_polarity = p->clk_polarity;
		inp.vsync_polarity = p->vsync_polarity;
		inp.hsync_polarity = p->hsync_polarity;
		inp.input_order = p->input_order;
		inp.half_frame_rate=p->half_frame_rate;  //only for nmi601
		
		if (p->blank_interval == 0) {
			/**
				Set default blank period
			**/
			if (inp.btrate == nClk13_5)
				inp.blank_interval = 512;
			else if (inp.btrate == nClk27)
				inp.blank_interval = 540;
		} else
			inp.blank_interval = p->blank_interval;
		inp.enablepattern = p->enable_pattern;
		inp.interlace = p->interlace;

		/**
			Power up
		**/
		nmi_power_up();

		/**
			initialize I2c
		**/
		if (nmi_i2c_init() < 0) {
			dPrint(N_ERR, "Failed, i2c initialization...\n");
			rc = NMI_RET_ERR_IO;
			goto _exit_;	
		}

		/**
			Asic Init
		**/
		inp.zone						= N_ERR|N_INFO;
		inp.hlp.write				= nmi_i2c_write;
		inp.hlp.read				= nmi_i2c_read;
		inp.hlp.delay				= nmi_os_delay;
		inp.hlp.gettick				= nmi_os_get_tick;
		inp.hlp.log					= nmi_os_print;
		nmi_common_init(&inp, &pd->atv);

		/**
			initialize chip
		**/
		if (pd->atv.init() < 0) {
			dPrint(N_ERR, "Failed, chip initialization...\n");
			rc = NMI_RET_ERR_IO;
			goto _exit_;
		}

		
		pd->chipid=pd->atv.getchipid();

		/**
			create a mutex for sync
		**/
		pd->mu = nmi_os_create_mutex();

		already_init = 1;
		dPrint(N_INFO, "Initialize Ok...sw version:%d.%d.%d.%d\n",
			        ASIC_MAJOR_VER,ASIC_MINOR_VER,ASIC_REV1,ASIC_REV2);



	}

_exit_:

	if (rc == NMI_RET_ERR_IO) {
		nmi_atv_deinit();
	}

	return rc;
}


void nmi_atv_set_alpha_adapt_factor(double factor)
{
	tNmiDrv *pd = &nmiDrv;

	uint32_t val32;
	uint32_t reg;
	uint32_t alpha;	

	nmi_os_lock_mutex(pd->mu);

   for(reg=0x31244;reg<=0x31260;reg+=4)
    {
		val32 = pd->atv.r32(reg);				

      #if 0  //lut
		alpha=(val32>>16) & 0x3fff;
		alpha= ((double)alpha)*factor;
		if(alpha>0x3fff) alpha=0x3fff;
		
		val32 &= ~(0x3fff << 16);		
		val32 |= (alpha << 16);		
		
		pd->atv.w32(reg, val32);
	  #else //thrd
		alpha= val32 & 0xffff;
		alpha= ((double)alpha)*factor;
		if(alpha>0xffff) alpha=0xffff;
		
		val32 &= ~(0xffff );		
		val32 |= (alpha);		
		
		pd->atv.w32(reg, val32);	
	  #endif

		val32 = pd->atv.r32(reg);		

    }

	nmi_os_unlock_mutex(pd->mu);
}



