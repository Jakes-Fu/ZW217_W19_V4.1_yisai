static void nmi_fm_post_process(tNmiFMProcess *p)
{ 
	tNmiDrv *pd = &nmiDrv;
	int aRssi[206],i,j;
	int *paRssi;
	
	dPrint(N_INFO, "FM Post Process: len (%d)\n", p->chnRssiLen);
	if (p->work)
		paRssi = p->work;
	else
		paRssi = aRssi;
 
	pd->atv.fmavgrssi(p->chnRssi, p->chnRssiLen, paRssi, p->chnOverTh);
	pd->atv.fmscanlist(p->chnRssi,
										paRssi,
										p->chnValid	,
										&p->chnValidCount,
										p->chnValidLen,
										p->chnRssiLen,
										p->chnOverTh);

	/**
		order
	**/	
	for (i = 0; i < p->chnValidCount - 1; i++) {
		for (j = 0; j < p->chnValidCount - 1; j++) {
			if (p->chnValid[j] > p->chnValid[j+1]) {
				int tmp;
				tmp = p->chnValid[j];
				p->chnValid[j] = p->chnValid[j+1];
				p->chnValid[j+1] = tmp;
			} 
		}
	}

	/**
		Store the frequency
	**/
	for (i = 0; i < p->chnValidCount - 1; i++) {
		int pos = p->chnValid[i];
		p->chnValid[i] = pd->pch[pos].frequency;
	}

	
	return;
}


static tNmiRetCode nmi_atv_scan(tNmiScanCallBack fnscan)
{
	tNmiDrv *pd = &nmiDrv;
	int i, lock, stop = 0;
	tNmiRetCode rc = NMI_RET_OK;

	if (!already_init) {
		dPrint(N_ERR, "Scan: Failed, not initialize...\n");
		return NMI_RET_ERR_IO;
	}

	if (pd->pch == NULL) {
		dPrint(N_ERR, "Scan: Failed, no set region...\n");
		return NMI_RET_BAD_PARAMETER;
	}

	nmi_os_lock_mutex(pd->mu);
	pd->atv.mute(1);

	for (i = 0; i < pd->cchc; i++) {
		tAtvTune tune;

		memset(&tune, 0, sizeof(tAtvTune));		
		tune.frequency = pd->pch[i].frequency;		
		tune.standard = pd->pch[i].standard;
		lock = pd->atv.scan(&tune);
		stop = fnscan(pd->pch[i].chn, lock, 0);
		if (stop)
			break;
	}
	pd->atv.forcecolor(1);

	/**
		do the last call back to indicate finish scan
	**/
	fnscan(0, 0, 1);

	pd->atv.mute(0);
	nmi_os_unlock_mutex(pd->mu);

	return rc;
}

