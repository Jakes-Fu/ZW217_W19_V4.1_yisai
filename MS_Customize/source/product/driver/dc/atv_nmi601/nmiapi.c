tNmiRetCode nmi_drv_ctl(uint32_t code, void *inp)
{
	tNmiRetCode rc = NMI_RET_OK;

	switch (code) {
	case NMI_DRV_INIT:
		if (inp != NULL) {
			rc = nmi_atv_init((tAtvInit *)inp);
		} else {
			rc = NMI_RET_BAD_PARAMETER;
		}
		break;
	case NMI_DRV_DEINIT:
		nmi_atv_deinit();
		break;
	case NMI_DRV_SET_REGION:
		if (inp != NULL)
			rc = nmi_atv_set_region((tAtvRegion *)inp);
		else
			rc = NMI_RET_BAD_PARAMETER;
		break;
	case NMI_DRV_BAND_SCAN:
		if (inp != NULL)
			rc = nmi_atv_scan((tNmiScanCallBack)inp);
		else
			rc = NMI_RET_BAD_PARAMETER;
		break;
	case NMI_DRV_TUNE_TO_CHANNEL:
		if (inp != NULL)
			rc = nmi_atv_tune((tNmiTune *)inp);
		else
			rc = NMI_RET_BAD_PARAMETER;
		break;
	case NMI_DRV_FM_SCAN:
		rc = nmi_fm_scan((tNmiFMScan *)inp);
		break;
	case NMI_DRV_FM_SEEK:
		rc = nmi_fm_seek((tNmiFMSeek *)inp);
		break;
	case NMI_DRV_FM_SEEK_SELECT:
		nmi_fm_seek_select((tNmiFMSeekSelect *)inp);
		break;
	case NMI_DRV_FM_PROCESS:
		nmi_fm_post_process((tNmiFMProcess *)inp);
		break;
	case NMI_DRV_GET_STATUS:
		if (inp != NULL)
			rc = nmi_atv_get_status((tNmiSq *)inp);
		else
			rc = NMI_RET_BAD_PARAMETER;
		break;
	case NMI_DRV_MUTE:
		nmi_atv_mute((tNmiMute *)inp);
		break;
	case NMI_DRV_UV_GAIN:
		nmi_atv_uv_gain((tNmiUVGain *)inp);
		break;
	case NMI_DRV_UV_SNR_THRESHOLD:
		nmi_atv_uv_snr_threshold((tNmiUVSnrTh *)inp);
		break;
	case NMI_DRV_SET_VOLUME:
		nmi_atv_set_volume((tNmiVolume *)inp);
		break;
	case NMI_DRV_FM_DIRECT_TUNE:
		rc = nmi_fm_direct_tune((tNmiFMTune *)inp);
		break;
	case NMI_DRV_FORCE_COLOR:
		nmi_atv_force_color((tNmiForceColor *)inp);
		break;
	case NMI_DRV_SHOW_PATTERN:
		nmi_atv_show_pattern((tNmiPattern *)inp);
		break;
	case NMI_DRV_SET_SCAN_LEVEL:
		nmi_atv_set_scan_level(*((int *)inp));
		break;
	case NMI_DRV_SET_UV_IIR_FACTOR:
		nmi_atv_set_uv_factor(*((double *)inp));
		break;
	case NMI_DRV_SET_AUDIO_FILTERS:
		nmi_atv_set_audio_filters((tNmiAudioFilters *)inp);
		break;
	case NMI_DRV_SET_OUTPUT_SIZE:
		nmi_atv_set_output_size((tBtSize *)inp);
		break;
	case NMI_DRV_SET_GAMMA:
		nmi_atv_set_gamma(*((int *)inp));
		break;
	case NMI_DRV_GET_RSSI:
		nmi_atv_get_rssi((int *)inp);
		break;
	case NMI_DRV_GET_VSYNC_LOCK:
		nmi_atv_get_vsync_lock((int *)inp);
		break;
	case NMI_DRV_GET_CN:
		nmi_atv_get_cn((double *)inp);
		break;
	case NMI_DRV_VERSION:
		if (inp != NULL) {
			tNmiDriverVer *pver = (tNmiDriverVer *)inp;
			pver->major = ASIC_MAJOR_VER;
			pver->minor = ASIC_MINOR_VER;
			pver->rev1 = ASIC_REV1;
			pver->rev2 = ASIC_REV2;
		} else 
			rc = NMI_RET_BAD_PARAMETER;
		break;
	default:
		rc = NMI_RET_BAD_PARAMETER;
		break;
	}

	return rc;

}
