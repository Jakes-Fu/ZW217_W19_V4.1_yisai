static tNmiRetCode nmi_atv_set_region(tAtvRegion *p)
{
	int i;
	tNmiDrv *pd = &nmiDrv;
	tNmiRetCode rc = NMI_RET_OK;

	if (!already_init) {
		dPrint(N_ERR, "set region: Failed, not initialize...\n");
 		return NMI_RET_ERR_IO;
	}

	dPrint(N_FUNC, "set region: new (%d), old (%d)\n", p->region, pd->cRgn);

	if (pd->cRgn == p->region) {
		p->schannel = pd->cschn;
		p->echannel = pd->cechn;
		return rc;
	}

	nmi_os_lock_mutex(pd->mu);
	for (i = 0; i < sizeof(aRgnMap)/sizeof(tNmiRegionMap); i++) {
		if (aRgnMap[i].region == p->region) {
			pd->cRgn = p->region;
			pd->pch = (tNmiAtvChannel *)aRgnMap[i].pch;
			break;
		}
	}

	if (pd->pch != NULL) {
		p->schannel = pd->pch[0].chn;
		for (i = 0;; i++) {
			if (pd->pch[i].chn == 0)
				break;
		}
		p->nchannel = pd->cchc = i;
		p->echannel = pd->pch[i-1].chn;
		dPrint(N_FUNC, "set region: channel, start (%d), end (%d)\n", p->schannel, p->echannel);

		pd->cschn = p->schannel;
		pd->cechn = p->echannel;
		
	} else {
		dPrint(N_ERR, "set region: Failed to set region...\n");
		rc = NMI_RET_BAD_PARAMETER;
	}
	nmi_os_unlock_mutex(pd->mu);

	return rc;
}

static tNmiRetCode nmi_fm_seek(tNmiFMSeek *p)
{
	tNmiDrv *pd = &nmiDrv;
	int ix, i, j, chn = p->chn;
	tNmiRetCode rc = NMI_RET_OK;
	tAtvTune tune;
	int Rssi[7], chnIndex[7], overth[7];
	int nc = 5, nc1 = 2;	

	if (!already_init) {
		dPrint(N_ERR, "FM Seek: Failed, not initialize...\n");
		return NMI_RET_ERR_IO;
	}

	if (pd->pch == NULL) {
		dPrint(N_ERR, "FM Seek: Failed, no set region...\n");
		return NMI_RET_BAD_PARAMETER;
	}

	nmi_os_lock_mutex(pd->mu);

	if (pd->cRgn == nFmThai) {
		nc = 7;
		nc1 = 3;
	}

	/**
		if first channel, clear the variables
	**/
	if (p->firstchannel) {
		for (i = 0; i < nc; i++) {
			pd->fmSeek[i].tix = -3;
			pd->fmSeek[i].rssi = -3;
			pd->fmSeek[i].overth = 65535;
		}
	}

	/**
		current channel index
	**/	
	ix = chn - pd->cschn;
	dPrint(N_SEEK, "channel: (%d), index (%d), frequency (%d)\n", chn, ix, pd->pch[ix].frequency);

	/**
		mute the audio
	**/	
	//pd->atv.mute(1);

	/**
		get the neighbor's rssi 
	**/
	for (i = -nc1, j = 0; i <= nc1; i++, j++) {
		int found = 0, k;

		for (k = 0; k < nc; k++) {
			if (pd->fmSeek[k].tix == ix+i) {
				Rssi[j] = pd->fmSeek[k].rssi;				
				overth[j] = pd->fmSeek[k].overth;  ////
				chnIndex[j] = ix+i;
				found = 1;
				break;
			}
		}

		if (!found) {
			memset(&tune, 0, sizeof(tAtvTune));

			dPrint(N_SEEK, "index (%d)\n", (ix+i));

			if (((ix+i) <= (pd->cchc-1)) && ((ix+i) >= 0)) {
				tune.frequency = pd->pch[ix+i].frequency;
				tune.standard = pd->pch[ix+i].standard;
			} else {
				if (nc == 7) {
					if ((ix+i) == -1) {
						tune.frequency = pd->pch[0].frequency - 50000;
					} else if ((ix+i) == -2) {
						tune.frequency = pd->pch[0].frequency - 100000;
					} else if ((ix+i) == -3) {
						tune.frequency = pd->pch[0].frequency - 150000;
					} else if ((ix+i) == pd->cchc) {
						tune.frequency = pd->pch[pd->cchc-1].frequency + 50000;
					} else if ((ix+i) == pd->cchc + 1) {
						tune.frequency = pd->pch[pd->cchc-1].frequency + 100000;
					} else if ((ix+i) == pd->cchc + 2) {
						tune.frequency = pd->pch[pd->cchc-1].frequency + 150000;
					} 

				} else {
					if ((ix+i) == -1) {
						tune.frequency = pd->pch[0].frequency - 100000;
					} else if ((ix+i) == -2) {
						tune.frequency = pd->pch[0].frequency - 200000;
					} else if ((ix+i) == pd->cchc) {
						tune.frequency = pd->pch[pd->cchc-1].frequency + 100000;
					} else if ((ix+i) == pd->cchc + 1) {
						tune.frequency = pd->pch[pd->cchc-1].frequency + 200000;
					}
				}
				tune.standard = nFM_RADIO;
			}
			pd->atv.tune(&tune);
			nmi_os_delay(20);
			Rssi[j] = pd->atv.getrssi();
			overth[j] = pd->atv.getoverthcount();
			chnIndex[j] = ix+i;
		} 
	}

#if 0
	/* this is for debugging */
	for (i = 0; i < nc; i++) {
		dPrint(N_SEEK, "Rssi[%d]=%d,%d\n", i, Rssi[i], chnIndex[i]);
	}
#endif

	p->found = pd->atv.fmseek(Rssi, overth, nc);
	
	p->rssi    = Rssi[nc1];
	dPrint(N_SEEK, "Seek: found (%d),(%d),(%d)\n", p->found,nc,p->rssi);
	/**
		save the current rssi info
	**/
	for (i = 0; i < nc; i++) {
		pd->fmSeek[i].tix = chnIndex[i];
		pd->fmSeek[i].rssi = Rssi[i];
		pd->fmSeek[i].overth = overth[i];
	}
	nmi_os_unlock_mutex(pd->mu);
	return rc;
}

