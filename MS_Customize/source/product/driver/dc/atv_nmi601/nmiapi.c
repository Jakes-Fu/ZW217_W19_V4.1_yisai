static uint32_t dflag = N_ERR | N_INFO | N_TUNE | N_FUNC | N_SEEK;
char buf[1024];
static void dPrint(uint32_t flag, char *fmt, ...)
{
	//char buf[256];
	va_list args;
	int len;

	if (flag & dflag) { 
		va_start(args, fmt);
		strcpy(buf, "[Nmi Api]: "); 
		len = vsprintf(&buf[11], fmt, args);
		va_end(args);
		nmi_os_print(buf);	
	}

	return;
}

