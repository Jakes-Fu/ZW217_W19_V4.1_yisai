static tNmiRetCode nmi_fm_scan(tNmiFMScan *p)
{
	tNmiDrv *pd = &nmiDrv;
	int chn, i;
	tNmiRetCode rc = NMI_RET_OK;
	tAtvTune tune;

	if (!already_init) {
		dPrint(N_ERR, "FM Scan: Failed, not initialize...\n");
		return NMI_RET_ERR_IO;
	}

	if (pd->pch == NULL) {
		dPrint(N_ERR, "FM Scan: Failed, no set region...\n");
		return NMI_RET_BAD_PARAMETER;
	}

	nmi_os_lock_mutex(pd->mu);

	for (i = 0; i < pd->cchc; i++) {
		if (pd->pch[i].chn == p->chn)
			break;
	}
	chn = i;

	pd->atv.mute(1);
	memset(&tune, 0, sizeof(tAtvTune));		
	tune.frequency = pd->pch[chn].frequency;		
	tune.standard = pd->pch[chn].standard;
	pd->atv.tune(&tune);
	nmi_os_delay(20);
	p->rssi = pd->atv.getrssi();
	p->overth = pd->atv.getoverthcount();
	dPrint(N_INFO, "FM: frequency (%d), rssi (%d), over th count (%d),  chn (%d)\n", pd->pch[chn].frequency, p->rssi, p->overth, chn);
	pd->atv.mute(0);

	nmi_os_unlock_mutex(pd->mu);

	return rc;
}

